print(a-b)          #Subtract 2 numbers
print(a*b)          #Multiply 2 numbers
print(a**b)         #Exponentiation
print(a//b)         #Floor division *round off the quotient to the nearest whole number*
print(a%b)          #Modulus *returns the remainder of the division*