# Type casting 

int_var = 10
float_var = float(int_var)  # Cast int to float
str_var = str(float_var)    # Cast float to string  

print(f"Integer: {int_var}, Float: {float_var}, String: '{str_var}'")

'''Basically python allows user to change type of variable according to their need
for that we use type casting functions like int(), float(), str() etc.'''